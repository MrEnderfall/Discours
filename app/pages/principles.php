    <br> Для конкретизации задач разработки был выработан ряд принципов, которые приведены ниже.

    <h2>Принцип свободы слова</h2>
    На Дискурсе приветствуется свобода слова. Ни одно мнение не может быть признано единственно верным.<br>
    <br>
    Тем не менее, постинг определенного контента усложняет общение пользователей, мешает работе форума, засоряет информационный поток.<br>
