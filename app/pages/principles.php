      <li><b>Спам.</b> В том числе пиар имиджборд. Единичные сообщения будут удаляться, в случае рецидивов авторы попадают в бан. Если вы хотите попиариться, сделать это можно в <a href="https://discou.rs/topic/1152" target="_blank">специальной теме</a>.</li>
